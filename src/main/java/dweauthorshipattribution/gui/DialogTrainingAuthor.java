    private void jButtonSelectFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelectFileActionPerformed
        // TODO add your handling code here:
        File selectFile = FileUtils.selectFile(this, "Text files", "txt");

        if(selectFile != null){
            try{
                selectedTextFile = new TextFile(selectFile);
                jLabelTextTrainingFile.setText(selectFile.getAbsolutePath());
            }catch(Exception ex){
                JOptionPane.showMessageDialog(this, "Error loading text file", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonSelectFileActionPerformed

    private void jButtonTrainingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTrainingActionPerformed
        // TODO add your handling code here:
        if(jTextFieldName.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Insert a author name!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(selectedTextFile != null){
            String createdTextFileName = jTextFieldName.getText().trim().replace(" ", "_");
            File locationToWrite = new File(System.getProperty("user.dir"));
            
            final DoTrainingIF doTraining =
                    new DoTraining(selectedTextFile, createdTextFileName, jTextFieldName.getText().trim(),
                    locationToWrite, wordClassifier);

            Thread t = new Thread(){

                public void run(){
                    try{
                        Thread tt = new Thread(){
                            public void run(){
                                dialogComicSplash.setVisible(true);
                            }
                        };

                        tt.start();

                        long before = System.currentTimeMillis();
                        doTraining.execute();
                        long after = System.currentTimeMillis();

                        String time = TimeUtils.convertInterval(before, after);

                        String message = String.format("Author training with sucess in %s!", time);
                        String createdFilePath = doTraining.getCreatedFilePath();
                        jLabelCreatedFile.setText(createdFilePath);

                        dialogComicSplash.setVisible(false);
                        JOptionPane.showMessageDialog(null, message, "Sucess", JOptionPane.INFORMATION_MESSAGE);
                        setVisible(false);
                    }catch(Exception ex){
                        ex.printStackTrace();
                        dialogComicSplash.setVisible(false);
                        JOptionPane.showMessageDialog(null, "Error training author! Verify the training file.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            };

            t.start();
        }else
            JOptionPane.showMessageDialog(this, "Select a text file!", "Error", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButtonTrainingActionPerformed

